from math import pow, sqrt


def soma(a1, b1):
    s = a1 + b1
    return print(s)


def subtracao(a2, b2):
    sub = a2 - b2
    return print(sub)


def mutiplicacao(e, f):
    m = e * f
    return print(m)


def divisao(g, h):
    d = g/h
    return print(d)


def potenciacao(n):
    return print(pow(n, 2))


def raiz(m):
    return print(sqrt(m))




